package com.example.laclicnote;

public class Time {
    private int yy;
    private int mm;
    private int dd;
    private int hrs;
    private int min;
    private int sec;

    public Time(int yy, int mm, int dd, int hrs, int min, int sec) {
        this.yy = yy;
        this.mm = mm;
        this.dd = dd;
        this.hrs = hrs;
        this.min = min;
        this.sec = sec;
    }
}
